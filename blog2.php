<p class="format">What is W3C Validation? </p><br/>
<p class="format">Markup is a validation service by W3C (World Wide Web Consortium) that helps check the validity and accuracy of Web documents.  Most Web documents are written using codes, or “Markup Languages”, such as HTML, XML, or XHTML. These languages are defined by technical specifications which usually include a machine-readable format and vocabulary. Validation is the act of checking a document against these constraints. </p><br/>
<p class="format">What are some of the benefits of W3C Validation?</p><br/>
<ul>
	<li><p class="format">Validated code ensures uniformity across all Web-browsers, so your site will always look as it was intended to look.</p></li>
	<li><p class="format">Validated code has a faster rendering/loading time, because the Web browser doesn’t have to do any proofreading or code-correcting.</p></li>
	<li><p class="format">Google picks up sites with validated code over non-validated code, so your site will be more visible when it is searched for.</p></li>
	<li><p class="format">Validation is the best way to ensure that your site will stand the test of time, ensuring that your site will look the same even as Web browser change and update over time</p></li>
